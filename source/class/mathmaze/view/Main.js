qx.Class.define("mathmaze.view.Main", {
    extend: qx.ui.container.Composite,

    /*
     *****************************************************************************
     STATICS
     *****************************************************************************
     */
    statics: {},

    /*
     *****************************************************************************
     CONSTRUCTOR
     *****************************************************************************
     */
    construct: function (layout) {
        this.base(arguments, layout || new qx.ui.layout.VBox(1));

        this.set({
            backgroundColor:"black",
            width:600,
            height:800
        });
    },

    /*
     *****************************************************************************
     EVENTS
     *****************************************************************************
     */
    events: {},

    /*
     *****************************************************************************
     PROPERTY
     *****************************************************************************
     */
    properties: {
    },

    /*
     *****************************************************************************
     MEMBERS
     *****************************************************************************
     */
    members: {
        _createChildControlImpl: function (id, hash) {
            var control;

            switch(id) {
                case "splash":
                    control = new mathmaze.view.Splash();
                    break;
                case "selectStage":
                    break;
                case "game":
                    break;
            }

            return control || this.base(arguments, id);
        }
    },

    /*
     *****************************************************************************
     DESTRUCT
     *****************************************************************************
     */
    destruct: function () {
    }
});