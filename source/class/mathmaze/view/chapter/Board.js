qx.Class.define("mathmaze.view.chapter.Board", {
    extend: qx.ui.container.Composite,

    /*
     *****************************************************************************
     STATICS
     *****************************************************************************
     */
    statics: {},

    /*
     *****************************************************************************
     CONSTRUCTOR
     *****************************************************************************
     */
    construct: function (layout) {
        this.base(arguments, layout || new qx.ui.layout.VBox());

        this.set({
            width:500
        });
    },

    /*
     *****************************************************************************
     EVENTS
     *****************************************************************************
     */
    events: {},

    /*
     *****************************************************************************
     PROPERTY
     *****************************************************************************
     */
    properties: {
    },

    /*
     *****************************************************************************
     MEMBERS
     *****************************************************************************
     */
    members: {
        _createChildControlImpl: function (id, hash) {
            var control;

            switch(id) {
                case "row":
                    control = qx.ui.container.Composite(new qx.ui.layout.VBox());
                    break;
                case "item":
                    control = new mathmaze.view.chapter.BoardItem();
                    break;
                case "selectStage":
                    break;
            }

            return control || this.base(arguments, id);
        }
    },

    /*
     *****************************************************************************
     DESTRUCT
     *****************************************************************************
     */
    destruct: function () {
    }
});